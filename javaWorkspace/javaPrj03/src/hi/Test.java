package hi;

public class Test {

	public static void main(String[] args) {
		
	
		/* 
		 * 산술연산자
		 * 숫자 연산 숫자 => 숫자
		 */
		
		System.out.println(10 + 3);   // 더하기
		System.out.println(10 - 3);   //빼기
		System.out.println(10 * 3); //곱하기
		System.out.println(10 / 3); //몫을 구하는 연산자
		System.out.println(10 % 3); //나머지를 구하는 연산자
		
        

		
	}

}
