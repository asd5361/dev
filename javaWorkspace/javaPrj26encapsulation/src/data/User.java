package data;

public class User {

	private int age;
	private String job;
	private String mbti;
	private String bobby;
	private int salary;
	private boolean isSkinHead;
	private char bloodType;
	private String id;
	private String pw;
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getMbti() {
		return mbti;
	}
	public void setMbti(String mbti) {
		this.mbti = mbti;
	}
	public String getBobby() {
		return bobby;
	}
	public void setBobby(String bobby) {
		this.bobby = bobby;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public boolean getIsSkinHead() {
		return isSkinHead;
	}
	public void setIsSkinHead(boolean isSkinHead) {
		this.isSkinHead = isSkinHead;
	}
	public char getBloodType() {
		return bloodType;
	}
	public void setBloodType(char bloodType) {
		this.bloodType = bloodType;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	
	
}
